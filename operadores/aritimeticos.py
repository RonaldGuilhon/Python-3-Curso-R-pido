x = 10
y = 3

# Operadores aritméticos:
# +3  → operador prefixado (exemplo: valor positivo)
# x++ → não existe em Python (existe em C/Java como pós-incremento)
# x + y → operador infixado (normal entre dois valores)

# Soma
print(x + y)  # 10 + 3 = 13

# Subtração
print(x - y)  # 10 - 3 = 7

# Multiplicação
print(x * y)  # 10 * 3 = 30

# Divisão com resultado float
print(x / y)  # 10 / 3 = 3.333...

# Módulo (resto da divisão)
print(x % y)  # 10 % 3 = 1

# Verificando se o número é par
par = 34
print(par % 2 == 0)  # True (34 é par)

# Verificando se o número é ímpar
impar = 33
print(impar % 2 == 1)  # True (33 é ímpar)
