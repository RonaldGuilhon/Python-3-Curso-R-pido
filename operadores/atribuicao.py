# Atribuição simples
resultado = 2

# Adição e atribuição
resultado += 3  # resultado = resultado + 3 → 2 + 3 = 5

# Subtração e atribuição
resultado -= 1  # resultado = resultado - 1 → 5 - 1 = 4

# Multiplicação e atribuição
resultado *= 4  # resultado = resultado * 4 → 4 * 4 = 16

# Divisão e atribuição (resultado em float)
resultado /= 2  # resultado = resultado / 2 → 16 / 2 = 8.0

# Módulo (resto da divisão) e atribuição
resultado %= 6  # resultado = resultado % 6 → 8.0 % 6 = 2.0

print(resultado)  # Saída final: 2.0
