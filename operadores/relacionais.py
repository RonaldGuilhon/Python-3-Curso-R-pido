x = 7
y = 5

# Operadores relacionais (retornam True ou False):

print(x > y)    # True  → 7 é maior que 5
print(x < y)    # False → 7 não é menor que 5
print(x <= y)   # False → 7 não é menor ou igual a 5
print(x >= y)   # True  → 7 é maior ou igual a 5
print(x == y)   # False → 7 não é igual a 5
print(x != y)   # True  → 7 é diferente de 5

# Comparação entre tipos diferentes (str e int)
print('5' != y) # True  → string '5' é diferente do número inteiro 5
