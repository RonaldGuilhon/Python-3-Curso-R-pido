# uniarios.py

y = 4

# Operador unário "not" inverte valores booleanos
print(not False)  # True
print(not True)   # False

# Operador unário "-" (negação numérica)
print(-3)         # -3 → mantém negativo
print(-y)         # -4 → y vale 4, então -y inverte para -4

# Operador unário "+" (sem efeito real, apenas reafirma o sinal positivo)
print(+3)         # 3


a = 10
print(-a)       # -10
print(+a)       # 10 (sem alteração)
print(not True) # False
print(not 0)    # True (0 é considerado "falso")
print(not 5)    # False (números ≠ 0 são "verdadeiros")
