# Verificando o tipo de dados com a função type()

print(type(1))         # <class 'int'> → número inteiro
print(type(1.1))       # <class 'float'> → número com ponto decimal (float)
print(type('texto'))   # <class 'str'> → string (texto)
print(type(False))     # <class 'bool'> → valor booleano (falso)
print(type(True))      # <class 'bool'> → valor booleano (verdadeiro)
